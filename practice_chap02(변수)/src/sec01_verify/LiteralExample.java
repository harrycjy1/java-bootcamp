package sec01_verify;

public class LiteralExample {

	public static void main(String[] args) {
		

		int var1 = 10;
		System.out.println("var1(10진수)="+ var1);
	    int var2 = 010;
	    System.out.println("var2(8진수)="+ var2);
	    int var3 = 0x10;
	    System.out.println("var3(16진수)"+ var3);
	
	}
	

}
