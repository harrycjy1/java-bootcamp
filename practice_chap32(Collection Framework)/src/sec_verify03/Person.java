package sec_verify03;

public class Person {

	private String 		name;
	private int 		age;
	private String 		handPhone;
	
	public Person(String name, int age, String handPhone) {
		this.name = name;
		this.age = age;
		this.handPhone = handPhone;
	}

	public String getName() {
		return name;
	}

	public int getAge() {
		return age;
	}

	public String getHandPhone() {
		return handPhone;
	}
	
	
}
